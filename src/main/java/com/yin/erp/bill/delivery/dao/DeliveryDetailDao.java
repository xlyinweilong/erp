package com.yin.erp.bill.delivery.dao;

import com.yin.erp.bill.common.dao.BaseBillDetailDao;
import com.yin.erp.bill.common.entity.po.BillDetailPo;
import com.yin.erp.bill.delivery.entity.po.DeliveryDetailPo;
import org.springframework.data.jpa.repository.Modifying;
import org.springframework.data.jpa.repository.Query;
import org.springframework.data.repository.query.Param;

import javax.annotation.Resource;
import java.util.List;

/**
 * 配货单详情
 *
 * @author yin
 */
@Resource
public interface DeliveryDetailDao extends BaseBillDetailDao<DeliveryDetailPo, String> {

    @Modifying
    @Query("delete from DeliveryDetailPo t where t.billId = :billId")
    @Override
    int deleteAllByBillId(@Param("billId") String billId);

    @Override
    List<BillDetailPo> findByBillId(String billId);
}
